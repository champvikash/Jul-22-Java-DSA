public class B_AdditionAssignmentAndIncrementOperation {
	public static void main(String[] args) {
		
		int x;
		x = 5;
		System.out.println("x : " + x);
		x = x + 13;
		System.out.println("x : " + x);
		
		
		int y;
		y = 5;
		System.out.println("y : " + y);
		y += 13;
		System.out.println("y : " + y);
		
		
		int i;
		i = 10;
		System.out.println("i : " + i);
		i = i+1;
		System.out.println("i : " + i);
		i += 1;
		System.out.println("i : " + i);		
		i++;
		System.out.println("i : " + i);		
		
		
		double String = 0;
		
		
	}
}
