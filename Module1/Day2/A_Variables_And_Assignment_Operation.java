public class A_Variables_And_Assignment_Operation {
	public static void main(String[] args) {
		
		int age;
		age = 23;
		System.out.println(age);
		age = 57;
		System.out.println(age);
		age = 45 + 57;
		System.out.println(age);
		
		int num;
		num = 5;
		System.out.println(num);
		num = age * 2;
		System.out.println(num);
		
	}
}
