public class B_Comparisonal_Operations_And_Boolean_DataType {
	public static void main(String[] args) {
	    
		System.out.println(3>5);
		System.out.println(3<5);
		System.out.println(3>=5);
		System.out.println(3<=5);
		System.out.println(3==5);
		System.out.println(3!=5);
		
		boolean res;
		res = 4==5;
		System.out.println(res);
		
	}
}
