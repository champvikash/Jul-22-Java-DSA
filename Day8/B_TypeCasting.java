import java.util.*;

public class B_TypeCasting {
	public static void main(String[] args) {
		
		System.out.println(2+'2');
		
		Character ch = 'a';
		
		System.out.println( "Like a Num:  " + (int)ch );
		System.out.println( "Like a Char: " + ch );
		
		int num=73;
		
		System.out.println( "Like a Num:  " + num );
		System.out.println( "Like a Char: " + (char)num );
		
	}
}
